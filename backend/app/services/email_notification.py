class EmailNotificationService:
    def __init__(
        self,
        settings: Settings | None = None,
        client: httpx.Client | None = None,
    ) -> None:
        self.settings = settings or get_settings()
        self.client = client

    def send_aud_ready_notification(
        self,
        project: Project,
        generated_document: GeneratedDocument,
    ) -> bool:
        if not self.settings.EMAIL_NOTIFICATIONS_ENABLED:
            logger.info(
                "AUD ready email notifications are disabled for project %s.",
                project.id,
            )
            return False

        notification_url = clean_email_field(self.settings.EMAIL_NOTIFICATION_URL)
        if not notification_url:
            logger.warning(
                "AUD ready email notification skipped for project %s because "
                "EMAIL_NOTIFICATION_URL is not configured.",
                project.id,
            )
            return False

        download_url = build_generated_document_download_url(
            project,
            generated_document,
            self.settings,
        )
        payload = build_aud_ready_email_payload(
            project,
            self.settings,
            download_url=download_url,
        )
        if payload is None:
            logger.info(
                "AUD ready email notification skipped for project %s because "
                "email_id is not available.",
                project.id,
            )
            return False

        try:
            response = self._post(notification_url, payload)
            response.raise_for_status()
        except Exception:
            logger.exception(
                "AUD ready email notification failed for project %s and document %s.",
                project.id,
                generated_document.id,
            )
            return False

        logger.info(
            "AUD ready email notification sent for project %s and document %s.",
            project.id,
            generated_document.id,
        )
        return True

    def _post(self, notification_url: str, payload: dict[str, Any]) -> httpx.Response:
        if self.client is not None:
            return self.client.post(notification_url, json=payload)

        timeout_seconds = max(self.settings.EMAIL_NOTIFICATION_TIMEOUT_SECONDS, 0.1)
        with httpx.Client(
            timeout=timeout_seconds,
            verify=get_email_ssl_verify(self.settings),
            trust_env=self.settings.EMAIL_NOTIFICATION_TRUST_ENV,
        ) as client:
            return client.post(notification_url, json=payload)


def parse_document_metadata(generated_document: GeneratedDocument) -> dict[str, Any]:
    if not generated_document.metadata_json:
        return {}

    try:
        parsed = json.loads(generated_document.metadata_json)
    except json.JSONDecodeError:
        return {}

    return parsed if isinstance(parsed, dict) else {}


def was_aud_ready_notification_sent(generated_document: GeneratedDocument) -> bool:
    metadata = parse_document_metadata(generated_document)
    notification_metadata = metadata.get(AUD_READY_METADATA_KEY)
    return (
        isinstance(notification_metadata, dict)
        and notification_metadata.get("status") == AUD_READY_SENT_STATUS
    )


def mark_aud_ready_notification_sent(
    session: Session,
    generated_document: GeneratedDocument,
) -> None:
    metadata = parse_document_metadata(generated_document)
    metadata[AUD_READY_METADATA_KEY] = {
        "status": AUD_READY_SENT_STATUS,
        "sent_at": utc_now().isoformat(),
    }
    generated_document.metadata_json = json.dumps(metadata)
    session.add(generated_document)
    session.commit()
    session.refresh(generated_document)


def notify_aud_ready_for_document(
    session: Session,
    project_id: str,
    generated_document: GeneratedDocument,
    service: EmailNotificationService | None = None,
) -> bool:
    if was_aud_ready_notification_sent(generated_document):
        return False

    project = session.get(Project, project_id)
    if project is None:
        logger.error(
            "AUD ready email notification skipped because project %s was not found.",
            project_id,
        )
        return False

    try:
        sent = (service or EmailNotificationService()).send_aud_ready_notification(
            project,
            generated_document,
        )
    except Exception:
        logger.exception(
            "AUD ready email notification failed unexpectedly for project %s "
            "and document %s.",
            project_id,
            generated_document.id,
        )
        return False

    if not sent:
        return False

    mark_aud_ready_notification_sent(session, generated_document)
    return True
