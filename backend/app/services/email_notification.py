from app.models import GeneratedDocument, Project

logger = logging.getLogger(__name__)

AUD_READY_SUBJECT = "Your AUD Has Been Generated"
AUD_READY_METADATA_KEY = "email_notification"
AUD_READY_SENT_STATUS = "aud_ready_sent"


def clean_email_field(value: str | None, fallback: str = "") -> str:
    normalized = " ".join((value or "").split())
    return normalized or fallback


def clean_optional_setting(value: str | None) -> str | None:
    if value is None:
        return None

    stripped = value.strip()
    return stripped or None


def get_email_ssl_verify(settings: Settings) -> bool | str:
    if not settings.EMAIL_NOTIFICATION_VERIFY_SSL:
        return False

    ca_bundle = clean_optional_setting(settings.EMAIL_NOTIFICATION_CA_BUNDLE)
    return ca_bundle or True


def build_generated_document_download_url(
    project: Project,
    generated_document: GeneratedDocument,
    settings: Settings | None = None,
) -> str | None:
    resolved_settings = settings or get_settings()
    base_url = clean_optional_setting(
        resolved_settings.EMAIL_NOTIFICATION_DOWNLOAD_BASE_URL
    )
    if base_url is None:
        return None

    project_id = quote(clean_email_field(project.id), safe="")
    document_id = quote(clean_email_field(generated_document.id), safe="")
    if not project_id or not document_id:
        return None

    return (
        f"{base_url.rstrip('/')}/projects/{project_id}/generated-documents/"
        f"{document_id}/download"
    )


def build_aud_ready_email_payload(
    project: Project,
    settings: Settings | None = None,
    *,
    download_url: str | None = None,
) -> dict[str, Any] | None:
    resolved_settings = settings or get_settings()
    to_email = clean_email_field(project.email_id)
    if not to_email:
        return None

    author_name = clean_email_field(project.name, "there")
    customer_name = clean_email_field(project.customer_name, "the selected customer")
    module_name = clean_email_field(project.module_name, "selected module")
    download_text_line = f"\n\nDownload link: {download_url}" if download_url else ""

    body = (
        f"Hello {author_name},\n\n"
        f"Your requested AUD for {customer_name} - {module_name} is now ready "
        "to download."
        f"{download_text_line}\n\n"
        "This notification is generated by AUDacle."
    )
    body_html = build_aud_ready_email_html(
        author_name=author_name,
        customer_name=customer_name,
        module_name=module_name,
        download_url=download_url,
    )

    return {
        "to_email": to_email,
        "from_email": clean_email_field(
            resolved_settings.EMAIL_NOTIFICATION_FROM,
            "audacle@oracle.com",
        ),
        "subject": AUD_READY_SUBJECT,
        "body": body,
        "body_html": body_html,
        "cc": None,
        "bcc": None,
        "replyto": None,
    }


